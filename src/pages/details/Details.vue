<script>
  import $ from 'jquery'
  import { TimelineLite } from 'gsap'
  import { scrollIntoView } from '../../util'
  import { changeVisibleMixin, similarCaseMixin } from '../../mixins'
  import Paragraph from '../public/Paragraph'
  import WhiteLogo from '../public/WhiteLogo'

  export default {
    components: {Paragraph, WhiteLogo},
    mixins: [changeVisibleMixin, similarCaseMixin],
    data () {
      return {
        similarNum: 3,
        tjImgKey: 'tj_thumb',
        schemeVisible: false,
        schemeAnimated: false,
      }
    },
    computed: {
      detailId () {
        return this.$route.params.id
      },
      detailData () {
        return this.$store.state.goodtime.find(item => item.id === this.detailId) || {
          sj1_title: '',
          sj2_title: '',
          sj3_title: '',
          sj41_title: '',
          sj42_title: '',
          sj_thumb: []
        }
      },
      paragraphs () {
        return [
          {content: this.detailData.sj2_title.split(/\r?\n/)},
          {content: this.detailData.sj3_title.split(/\r?\n/)}
        ]
      }
    },
    mounted () {
      this.initSlick()
    },
    methods: {
      changeVisible () {
        this.schemeVisible = scrollIntoView(this.$refs.scheme)
      },
      schemeAnimate () {
        this.schemeAnimated = true

        const timeline = new TimelineLite()
        const {scheme, title, content} = this.$refs
        timeline
          .set(scheme, {autoAlpha: 1})
          .from(title, .5, {autoAlpha: 0}, '-=.5')
          .staggerFrom(content.querySelectorAll('span'), .4, {y: 30, autoAlpha: 0}, .01)
      },
      toMoreCase () {
        this.$router.push({path: '/wonderfulMoment'})
      },
      isPunctuation (char) {
        return ['，', '。', '？', '！',].includes(char)
      },
      initSlick () {
        $('#slicker').slick({
          slidesToShow: 1,
          autoplay: true,
          autoplaySpeed: 1000,
          dots: false,
          infinite: true,
          speed: 500,
          fade: true,
          cssEase: 'linear'
        })
      }
    },
    watch: {
      schemeVisible (newVal) {
        newVal && !this.schemeAnimated && this.schemeAnimate()
      },
      detailData () {
        try {
          $('#slicker').slick('unslick')
        } catch (e) {}
        setTimeout(this.initSlick.bind(this))
      }
    }
  }
</script>

